package com.example.quickvet.Data;

public class Clinic {

    private String Doctor_ID;
    private String Clinic_Name;
    private String Contact_Number;
    private String Clinic_Location;
    private String Clinic_Address;
    private String Profile_Image;

    public String getDoctor_ID() {
        return Doctor_ID;
    }

    public void setDoctor_ID(String doctor_ID) {
        Doctor_ID = doctor_ID;
    }

    public String getClinic_Name() {
        return Clinic_Name;
    }

    public void setClinic_Name(String clinic_Name) {
        Clinic_Name = clinic_Name;
    }

    public String getContact_Number() {
        return Contact_Number;
    }

    public void setContact_Number(String contact_Number) {
        Contact_Number = contact_Number;
    }

    public String getClinic_Location() {
        return Clinic_Location;
    }

    public void setClinic_Location(String clinic_Location) {
        Clinic_Location = clinic_Location;
    }

    public String getClinic_Address() {
        return Clinic_Address;
    }

    public void setClinic_Address(String clinic_Address) {
        Clinic_Address = clinic_Address;
    }

    public String getProfile_Image() {
        return Profile_Image;
    }

    public void setProfile_Image(String profile_Image) {
        Profile_Image = profile_Image;
    }
}
